package org.arunm.wiremockdemo;

public class FancyRunner {

    private String runnerId;
    private String runnerFancyName;
    private String runnerFancyBadge;

    public String getRunnerId() {
        return runnerId;
    }

    public void setRunnerId(String runnerId) {
        this.runnerId = runnerId;
    }

    public String getRunnerFancyName() {
        return runnerFancyName;
    }

    public void setRunnerFancyName(String runnerFancyName) {
        this.runnerFancyName = runnerFancyName;
    }

    public String getRunnerFancyBadge() {
        return runnerFancyBadge;
    }

    public void setRunnerFancyBadge(String runnerFancyBadge) {
        this.runnerFancyBadge = runnerFancyBadge;
    }
}
